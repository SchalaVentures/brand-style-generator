/** Combined list - gradients first (trending), then solid families */
export const allColorOptions: ColorOption[] = [...gradientFamilies, ...solidFamilies]

export function getColorOption(optionId: string): ColorOption | undefined {
  return allColorOptions.find((o: ColorOption) => o.id === optionId)
}

export function getSolidFamily(familyId: string): ColorFamily | undefined {
  return solidFamilies.find((f: ColorFamily) => f.id === familyId)
}

export function getGradientFamily(familyId: string): GradientFamily | undefined {
  return gradientFamilies.find((f: GradientFamily) => f.id === familyId)
}

export function getColorShade(familyId: string, shadeId: string): ColorShade | undefined {
  const family: ColorFamily | undefined = getSolidFamily(familyId)
  return family?.shades.find((s: ColorShade) => s.id === shadeId)
}

export function getAllShades(): ColorShade[] {
  return solidFamilies.flatMap((f: ColorFamily) => f.shades)
}
